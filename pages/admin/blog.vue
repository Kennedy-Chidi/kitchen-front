      content: "",
      blogs: [],
      date: "",
      author: "",

      showOverlay: false,
      overlayMsg: "",

      editingId: "",
      editingState: false,
      response: "",
      isError: false,
      showResponse: false,
      onRequest: false,
      showEditor: false,
      deleteId: "",

      sort: "-time",
      field: "",
      limit: 3,
      resultLength: "",
      currentPage: 1,
      pages: function () {
        let array = [];
        let x = Math.ceil(this.resultLength / this.limit);
        for (let i = 0; i < x; i++) {
          array.push("i");
        }
        return array;
      },
    };
  },
  methods: {
    formatDate(data) {
      if (data == null || data == undefined) {
        return "N/A";
      }
      const date = new Date(data).getTime(); // Note that month is zero-indexed in JavaScript
      const options = {
        year: "numeric",
        month: "long",
        day: "numeric",
        suffix: (day) => {
          if (day === 1 || day === 21 || day === 31) {
            return "st";
          } else if (day === 2 || day === 22) {
            return "nd";
          } else if (day === 3 || day === 23) {
            return "rd";
          } else {
            return "th";
          }
        },
      };

      return new Intl.DateTimeFormat("en-US", options).format(date);
    },

    showResponseMsg(msg, status) {
      this.response = msg;
      this.isError = status;
      this.showResponse = true;
      this.onRequest = false;
      setTimeout(() => {
        this.response = "";
        this.showResponse = false;
      }, 6000);
    },

    setBlogType(blogType) {
      this.blogType = blogType;
      this.showBlogTypeList = false;
    },

    setImage(event) {
      this.banner = event.target.files[0];
    },

    clearInputs() {
      this.editingId = "";
      this.editingState = false;
      this.blogType = "Select Type";
      (this.category = ""), (this.title = "");
      this.subtitle = "";
      this.banner = "";
      this.content = "";
      this.date = "";
      this.author = "";
    },

    startDelete(id) {
      this.deleteId = id;
      this.overlayMsg = "Are you sure you want to delete this blog?";
      this.showOverlay = true;
    },

    closeOverlay() {
      this.showOverlay = false;
      this.overlayMsg = "";
      this.deleteBlog();
    },

    prepareBlog(blog) {
      this.editingId = blog._id;
      this.editingState = true;
      this.banner = blog.banner;
      this.blogType = blog.blogType;
      this.title = blog.title;
      this.subtitle = blog.subtitle;
      this.content = blog.content;
      this.author = blog.author;
      this.category = blog.category;
      this.time = blog.time;
    },

    duplicateBlog(blog) {
      this.banner = blog.banner;
      this.blogType = blog.blogType;
      this.title = blog.title;
      this.subtitle = blog.subtitle;
      this.content = blog.content;
      this.author = blog.author;
      this.category = blog.category;
      this.time = blog.time;
    },

    sortResult() {
      this.sort = this.sort == "-time" ? "time" : "-time";
      this.getBlog();
    },

    paginate(page) {
      this.currentPage = page;
      this.getBlog();
    },

    processBlog() {
      this.onRequest = true;
      const form = new FormData();
      form.append("title", this.title);
      form.append("subtitle", this.subtitle);
      form.append("banner", this.banner);
      form.append("content", this.content);
      form.append("category", this.category);
      form.append("author", this.author);

      form.append(
        "time",
        this.date == "" ? new Date().getTime() : new Date(this.date).getTime()
      );
      if (this.editingState) {
        this.updateBlog(form);
      } else {
        this.createBlog(form);
      }
    },

    async updateBlog(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.patch(
          `/blogs/${this.editingId}/${query}`,
          form
        );
        this.clearInputs();
        this.blogs = result.data.data;
        this.resultLength = result.data.resultLength;
        this.showResponseMsg("Blog updated successfully", false);
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async createBlog(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.post(`/blogs/${query}`, form);
        this.clearInputs();
        this.blogs = result.data.data;
        this.resultLength = result.data.resultLength;
        this.showResponseMsg("Blog created successfully", false);
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async getBlog() {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.get(`/blogs/${query}`);
        this.blogs = result.data.data;
        this.resultLength = result.data.resultLength;
      } catch (err) {
        console.log(err.response.data.message);
      }
    },

    async deleteBlog() {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.delete(
          `/blogs/${this.deleteId}/${query}`
        );
        this.blogs = result.data.data;
        this.resultLength = result.data.resultLength;
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },
  },

  mounted() {
    this.getBlog();
  },

