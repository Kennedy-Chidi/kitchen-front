    HorizontalNav,
  },
  data() {
    return {
      sms: [],
      editState: false,
      editId: "",
      content: "",
      template: "",
      title: "",

      sort: "-time",
      limit: 5,
      resultLength: "",
      currentPage: 1,
      pages: function () {
        let array = [];
        let x = Math.ceil(this.resultLength / this.limit);
        for (let i = 0; i < x; i++) {
          array.push("i");
        }
        return array;
      },

      response: "",
      isError: false,
      showResponse: false,
      onRequest: false,
      showEditor: false,

      showOverlay: false,
      overlayMsg: "",
    };
  },

  methods: {
    showResponseMsg(msg, status) {
      this.response = msg;
      this.isError = status;
      this.showResponse = true;
      this.onRequest = false;
      setTimeout(() => {
        this.response = "";
        this.showResponse = false;
      }, 6000);
    },

    clearInputs() {
      this.editId = "";
      this.editState = false;
      this.title = "";
      this.greeting = "";
      this.banner = "";
      this.warning = "";
      this.template = "";
      this.content = "";
      this.deleteId = "";
    },

    sortResult() {
      this.sort = this.sort == "-time" ? "time" : "-time";
      this.getSMS();
    },

    paginate(page) {
      this.currentPage = page;
      this.getSMS();
    },

    duplicateSMS(sms) {
      this.content = sms.content;
      this.title = sms.title;
      this.template = sms.template;
    },

    prepareSMSEdit(sms) {
      this.editState = true;
      this.editId = sms._id;
      this.content = sms.content;
      this.template = sms.template;
      this.title = sms.title;
    },

    startDeleteSMS(id) {
      this.deleteId = id;
      this.showOverlay = true;
      this.overlayMsg = `Are you sure you want to delete this sms?`;
    },

    processSMS() {
      this.onRequest = true;
      const form = {
        title: this.title,
        content: this.content,
        template: this.template,
      };

      if (this.editState) {
        this.updateSMS(form);
      } else {
        this.createSMS(form);
      }
    },

    closeOverlay() {
      this.showOverlay = false;
      this.overlayMsg = "";
      if (this.deleteId != "") {
        this.deleteSMS();
      }
    },

    async createSMS(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=-dateCreated`;
      try {
        const result = await this.$axios.post(`/sms/${query}`, form);
        this.sms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.clearInputs();
        this.showResponseMsg("SMS saved successfully", false);
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async updateSMS(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}`;
      try {
        const result = await this.$axios.patch(
          `/sms/${this.editId}/${query}`,
          form
        );
        this.sms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.clearInputs();
        this.showResponseMsg("Email updated successfully", false);
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async getSMS() {
      const query = `?limit=${this.limit}&page=${this.currentPage}`;
      try {
        const result = await this.$axios.get(`/sms/${query}`);
        this.sms = result.data.data;
        this.resultLength = result.data.resultLength;
      } catch (err) {
        console.log(err.response.data);
      }
    },

    async deleteSMS() {
      const query = `?limit=${this.limit}&page=${this.currentPage}`;
      try {
        const result = await this.$axios.delete(
          `/sms/${this.deleteId}/${query}`
        );
        this.sms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.clearInputs();
      } catch (err) {
        this.showResponseMsg(err.response.data);
      }
    },
  },
  mounted() {
    this.getSMS();
  },
};
