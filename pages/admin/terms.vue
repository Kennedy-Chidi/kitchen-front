  data() {
    return {
      category: "",
      title: "",
      content: "",

      terms: [],

      editingId: "",
      editingState: false,
      showEditor: false,

      response: "",
      isError: false,
      showResponse: false,
      onRequest: false,

      showOverlay: false,
      overlayMsg: "",

      sort: "-dateCreated",
      limit: 3,
      resultLength: "",
      currentPage: 1,
      pages: function () {
        let array = [];
        let x = Math.ceil(this.resultLength / this.limit);
        for (let i = 0; i < x; i++) {
          array.push("i");
        }
        return array;
      },
    };
  },

  methods: {
    clearInputs() {
      this.title = "";
      this.content = "";
      this.category = "";
      this.editingState = false;
      this.editingId = "";
      this.deleteId = "";
    },

    showResponseMsg(msg, status) {
      this.response = msg;
      this.isError = status;
      this.showResponse = true;
      this.onRequest = false;
      setTimeout(() => {
        this.response = "";
        this.showResponse = false;
      }, 6000);
    },

    returnConfirmation(data) {
      if (data == "yes") {
        this.deleteTerms();
      }
      this.confirmStatus = true;
    },

    toggleTermsStatus(id, status) {
      this.editingId = id;
      this.editingState = true;
      const form = {
        status: !status,
      };

      this.updateTerms(form);
    },

    prepareTermEdit(term) {
      this.category = term.category;
      this.content = term.content;
      this.title = term.title;
      this.editingId = term._id;
      this.editingState = true;
    },

    sortResult() {
      this.sort = this.sort == "-time" ? "time" : "-time";
      this.getTerms();
    },

    paginate(page) {
      this.currentPage = page;
      this.getTerms();
    },

    processTerms() {
      this.onRequest = true;
      const form = {
        category: this.category,
        content: this.content,
        title: this.title,
      };

      if (this.editingState) {
        this.updateTerms(form);
      } else {
        this.createTerms(form);
      }
    },

    startDeleteTerms(id) {
      this.deleteId = id;
      this.showOverlay = true;
      this.overlayMsg = "Are you sure you want to delete this term?";
    },

    closeOverlay() {
      this.showOverlay = false;
      this.overlayMsg = "";
      this.deleteTerms();
    },

    async updateTerms(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.patch(
          `/terms/${this.editingId}/${query}`,
          form
        );
        this.terms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.clearInputs();
      } catch (err) {
        console.log(err.response.data.message);
      }
    },

    async createTerms(form) {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.post(`/terms/${query}`, form);
        this.terms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.showResponseMsg("The terms was created successfully", false);
        this.clearInputs();
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async getTerms() {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.get(`/terms/${query}`);
        this.terms = result.data.data;
        this.resultLength = result.data.resultLength;
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },

    async deleteTerms() {
      const query = `?limit=${this.limit}&page=${this.currentPage}&sort=${this.sort}`;
      try {
        const result = await this.$axios.delete(
          `/terms/${this.deleteId}/${query}`
        );
        this.terms = result.data.data;
        this.resultLength = result.data.resultLength;
        this.clearInputs();
      } catch (err) {
        this.showResponseMsg(err.response.data.message, true);
      }
    },
  },

  mounted() {
    this.getTerms();
  },

  computed: {
    editorConfig() {
      return this.$store.state.editor.editorConfig;
